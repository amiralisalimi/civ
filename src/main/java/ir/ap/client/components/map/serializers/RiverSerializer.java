package ir.ap.client.components.map.serializers;

public class RiverSerializer {
    private boolean up;
    private boolean upRight;
    private boolean downRight;
    private boolean down;
    private boolean downLeft;
    private boolean upLeft;

    public boolean getUp() {
        return up;
    }

    public boolean getUpRight() {
        return upRight;
    }

    public boolean getDownRight() {
        return downRight;
    }

    public boolean getDown() {
        return down;
    }

    public boolean getDownLeft() {
        return downLeft;
    }

    public boolean getUpLeft() {
        return upLeft;
    }
   
}