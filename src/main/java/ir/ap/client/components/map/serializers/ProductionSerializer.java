package ir.ap.client.components.map.serializers;

public class ProductionSerializer {
    private String name;
    private int id;
    private int cost;
    private int turnsForConstruction;

    public String getName() {
        return name;
    }
    public int getId() {
        return id;
    }
    public int getCost() {
        return cost;
    }
    public int getTurnsForConstruction() {
        return turnsForConstruction;
    }
}
